package Curso;

public class Arrays {

	public static void main(String[] args) {
		int[] arrayNumeros = {10, 20, 30, 40, 50};
		String[] arrayString = {"Batata", "Arroz"};
		
		System.out.println("Array Números:");
		System.out.println("Na posição 1 (indice 0) -> " + arrayNumeros[0]);
		System.out.println("");
		System.out.println("Array Strings:");
		System.out.println(arrayString[1]);
	}
}
