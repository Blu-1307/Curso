    private void btnInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInserirActionPerformed
        try {
            if (!txtNomeAluno.getText().trim().isEmpty() && !txtNota.getText().trim().isEmpty() && !txtMateria.getText().trim().isEmpty()) {
                if (Double.parseDouble(txtNota.getText()) >= 0 && Double.parseDouble(txtNota.getText()) <= 10) {

                    if (indiceAlunoSelecionado > -1) {
                        getAluno(indiceAlunoSelecionado).setNome(txtNomeAluno.getText());
                        getAluno(indiceAlunoSelecionado).setMateria(txtMateria.getText());
                        getAluno(indiceAlunoSelecionado).setNota(Double.parseDouble(txtNota.getText()));
                        indiceAlunoSelecionado = -1;
                    } else {
                        Aluno aluno = new Aluno(txtNomeAluno.getText(), CONST_TURMA, txtMateria.getText(), Double.parseDouble(txtNota.getText()));

                        listaAlunos.add(aluno);
                    }
                    atualizaLista();
                } else {
                    JOptionPane.showMessageDialog(this, "A Nota digitada √© Maior/Menor que o permitido", "Nota inv√°lida!", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Um ou mais campos est√£o vazios \nPreencha todos por favor!", "Campos em branco", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro! " + e.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_btnInserirActionPerformed

    private void btnLimparCamposActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparCamposActionPerformed
        if(listAlunos.getSelectedIndex() > -1) {
            listAlunos.clearSelection();
        }
        limparCampos();
    }//GEN-LAST:event_btnLimparCamposActionPerformed

    private void btnLimparTudoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLimparTudoActionPerformed
        String[] limpo = {""};

        indiceAlunoSelecionado = -1;
        listAlunos.setListData(limpo);
        listaAlunos.removeAll(listaAlunos);
    }//GEN-LAST:event_btnLimparTudoActionPerformed

    private void btnExcluirRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirRegistroActionPerformed
        if (listAlunos.getSelectedIndex() > -1) {
            int indiceSelecionado = listAlunos.getSelectedIndex();
            listaAlunos.remove(indiceSelecionado);
            atualizaLista();
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um Registro!", "Registro n√£o selecionado", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirRegistroActionPerformed

