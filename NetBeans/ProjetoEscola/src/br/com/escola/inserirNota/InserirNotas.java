    public void atualizaLista() {
        String[] listaAlunosString = new String[listaAlunos.size()];

        for (int i = 0; i < listaAlunos.size(); i++) {
            listaAlunosString[i] = "[" + (i + 1) + "] " + getAluno(i).getNome();
            listaAlunosString[i] += " - " + getAluno(i).getMateria();
            listaAlunosString[i] += " : " + getAluno(i).getNota();
        }

        listAlunos.setListData(listaAlunosString);
        limparCampos();
    }

    public Aluno getAluno(int indice) {
        return listaAlunos.get(indice);
    }

    public void limparCampos() {
        txtMateria.setText("");
        txtNota.setText("");
        txtNomeAluno.setText("");
    }

